def add(a,b):
    answer = a + b
    print(str(a) + "+" + str(b) + "=" + str(answer) + "\n")

def subtract(a, b):
    answer = a - b
    print(str(a) + "-" + str(b) + "=" + str(answer) + "\n")

def multiply(a, b):
    answer = a * b
    print(str(a) + "*" + str(b) + "=" + str(answer) + "\n")

def divide(a, b):
    answer = a / b
    print(str(a) + "/" + str(b) + "=" + str(answer) + "\n")

def square(x):
    answer = x * x
    print(f"The sqaure of {x} is {answer}")

